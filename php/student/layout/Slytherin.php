	<h2> Slytherin </h2>

		<p id="p_house"> 
			Slytherin is one of the four Houses at Hogwarts School of Witchcraft and Wizardry. 
			Founded by Salazar Slytherin, the house is composed mostly of pure-blood students, due to its founder's 
			mistrust of Muggle-borns. The house is traditionally home to students who exhibit such traits as 
			cunning, resourcefulness, and ambition. Its emblematic animal is a snake and its colours are green and 
			silver. The house ghost is the Bloody Baron and its gem is the emerald.

			Slytherin corresponds roughly with the element of water with serpents being commonly associated with 
			the sea and lochs in western European mythology, as well as serpents being physically fluid and flexible 
			animals. The colours also correspond with waters around lakes and lochs often being green, and silver 
			being often associated with grey rain water.
		</p>

	<h3>Common Room</h3>

		<p>
			The Slytherin common room is in the dungeons of Hogwarts Castle and underneath the Black Lake. 
			Because of this, the light in the common room is green. To enter the Slytherin common room one 
			must merely speak aloud the current password in front 
			of a stretch of damp stone wall; once the word is uttered a concealed stone door will slide aside
			leaving a rectangular hole in the wall leading to the Slytherin common room. 
		</p> 
